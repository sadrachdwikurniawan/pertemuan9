<?php 
	function penjumlahan($bilA, $bilB){
		$hasil = 0;
		$hasil = $bilA + $bilB;
		return $hasil;
	}
	function pengurangan($bilA, $bilB){
		$hasil = 0;
		$hasil = $bilA - $bilB;
		return $hasil;
	}
	function perkalian($bilA, $bilB){
		$hasil = 0;
		$hasil = $bilA * $bilB;
		return $hasil;
	}
	function pembagian($bilA, $bilB){
		$hasil = 0;
		$hasil = $bilA / $bilB;
		return $hasil;
	}
 ?>